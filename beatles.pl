aniodealbum(X,Y):-
lanzado(Y,X).

listaanio(X,Lanzado):-
findall(Y,lanzado(Y,X),Lanzado);
Lanzado = none.

lista_rol(X,Rol):-
findall(Y,rol(Y,X),Rol);
Rol = none.

lista_album(X,Album):-
findall(Y,pertenece(X,Y),Album);
Album = none.

lista_inte(X,Integrantes):-
findall(Y,integrante(Y,X),Integrantes);
Integrantes = none.

acerca_de(X,Y,Z,W):-
    originario(X,Y),
    format("Originario ~w",[Y]),nl,
    lista_album(X,Z),
    format("Albums ~w",[Z]),nl,
    lista_inte(X,W),
    format("Integrantes ~w",[W]),nl,
    !.

cancion_album(X,Canciones):-
	findall(Y,cancionde(Y,X),Canciones);
	Canciones = none.

cancion_art(X,W):-
	pertenece(X,Y),
	cancionde(W,Y).

cancion_banda(X,Canciones):-
	findall(Y,cancionart(X,Y),Canciones);
	Canciones = none.


cancion_artista(X,W,Z,Y):-
	cancionde(X,W),
	format("Album ~w",[W]),nl,
	pertenece(Z,W),
	format("Artista ~w",[Z]),nl,
	lanzado(W,Y),
	format("Anio ~w",[Y]),nl,
	!.









